using System;

namespace MathTest
{
    public class SimpleMath
    {
        public SimpleMath()
        {
            Console.WriteLine();
        }
        public int Add(int a, int b)
        {
            return a + b;
        }

        public int Minus(int a, int b)
        {
            return a - b;
        }

        public int Multiply(int a, int b)
        {
            return a * b;
        }

        public int Divide(int a, int b)
        {
            return a / b;
        }
    }

}